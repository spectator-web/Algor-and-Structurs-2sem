def is_valid_solution(prices, result):
    """
    Проверяет корректность решения для задачи о купонах.
    result должен быть кортежем (min_cost, k1, k2, used_days).
    Проверяет:
    - суммарная стоимость равна min_cost,
    - количество использованных купонов = k2,
    - остаток купонов = k1,
    - used_days отсортированы по возрастанию,
    - все дни из used_days в пределах [1, n],
    - для каждого дня, когда использован купон, должен быть доступен купон
      (т.е. количество ранее полученных купонов минус ранее использованных >= 1),
    - в конце остаётся ровно k1 купонов.
    """
    if not isinstance(result, tuple) or len(result) != 4:
        return False
    min_cost, k1, k2, used_days = result

    n = len(prices)
    if not all(1 <= day <= n for day in used_days):
        return False
    if len(used_days) != k2:
        return False
    if sorted(used_days) != used_days:
        return False

    # симуляция
    coupons = 0
    cost_sum = 0
    used_set = set(used_days)
    for day in range(1, n + 1):
        s = prices[day - 1]
        if day in used_set:
            # используем купон – бесплатно
            if coupons == 0:
                return False  # нет купона для использования
            coupons -= 1
            # стоимость не добавляется
        else:
            # платим
            cost_sum += s
            if s > 100:
                coupons += 1

    if coupons != k1:
        return False
    if cost_sum != min_cost:
        return False
    return True


class TestCafeCoupons(unittest.TestCase):

    def setUp(self):
        random.seed(42)
        tracemalloc.clear_traces()

    def measure_performance(self, func, arg):
        """Измерение времени и памяти для функции, принимающей список цен."""
        start_time = time.perf_counter()
        result = func(arg)
        end_time = time.perf_counter()
        exec_time = end_time - start_time

        tracemalloc.start()
        _ = func(arg)
        current, peak = tracemalloc.get_traced_memory()
        tracemalloc.stop()

        return result, exec_time, peak

    def test_basic_cases(self):
        """Простые случаи с малым n."""
        print(f"\n{'ТЕСТИРОВАНИЕ БАЗОВЫХ СЛУЧАЕВ':^60}")
        print(f"{'=' * 60}")

        test_cases = [
            ("n=0", [], (0, 0, 0, [])),
            ("n=1, цена ≤100", [50], (50, 0, 0, [])),
            ("n=1, цена >100", [150], (150, 1, 0, [])),
            ("n=2, оба ≤100", [50, 70], (120, 0, 0, [])),
            ("n=2, первый >100, второй ≤100", [150, 70], (150, 0, 1, [2])),
            ("n=2, первый >100, второй ≤100, можно использовать купон", [150, 70], (150, 0, 1, [2])),
            ("n=2, оба >100", [150, 200], (150, 0, 1, [2])),  # купоны не используются
            ("n=2, оба >100, использовать купон на второй", [150, 200], (150, 0, 1, [2])),  # не выгодно
            # более хитрый: первый >100, второй ≤100 – выгодно использовать купон на второй
        ]

        for description, prices, expected in test_cases:
            with self.subTest(description=description):
                print(f"\n{description}: {prices} -> ожидаем {expected}")
                result, exec_time, mem_peak = self.measure_performance(solve, prices)
                print(f"Получено: {result}, время: {exec_time:.6f} сек, память: {mem_peak/1024:.2f} КБ")
                self.assertEqual(result, expected)
                self.assertTrue(is_valid_solution(prices, result))

    def test_choice_between_equal_cost(self):
        """При равной стоимости должно выбираться решение с бóльшим k1."""
        # Пример: цены [100, 50, 150]
        # Дни: 1:100 (не даёт купон), 2:50 (не даёт), 3:150 (даёт купон)
        # Варианты:
        # - заплатить за все: 100+50+150=300, остаток 1 купон
        # - использовать купон на день 2: платим 100+150=250, остаток 0 (купон использован)
        # - использовать купон на день 1? нельзя, т.к. день 1 не дал купона (купон появляется после покупки >100, т.е. в день 3)
        # На самом деле купон, полученный в день 3, можно использовать только в последующие дни, а дней после 3 нет.
        # Поэтому единственный способ – не использовать купон. Итого стоимость 300, k1=1.
        # Придумаем другой пример: [150, 50, 70]
        # Дни: 1:150 (купон), 2:50, 3:70.
        # Варианты:
        # - не использовать купон: платим 150+50+70=270, остаток 1 купон
        # - использовать купон на день 2: платим 150+70=220, остаток 0
        # - использовать купон на день 3: платим 150+50=200, остаток 0 (лучше)
        # Стоимость 200 лучше, чем 270, значит выбираем min cost = 200, k1=0.
        # При равной стоимости? Например, [150, 100, 100]?
        # Дни: 1:150 (купон), 2:100, 3:100.
        # Варианты:
        # - не использовать купон: 350, остаток 1
        # - использовать купон на день 2: 150+100=250, остаток 0
        # - использовать купон на день 3: 150+100=250, остаток 0
        # Стоимость 250 < 350.
        # Нужна ситуация, где две стратегии дают одинаковую стоимость, но разный остаток.
        # Например: [150, 150, 50, 50]
        # Дни: 1:150 (купон), 2:150 (купон), 3:50, 4:50.
        # Сумма всех = 400.
        # Варианты использования купонов:
        # - использовать купон на день 3: платим 150+150+50=350, остаток 1 (неиспользованный купон от дня 1 или 2)
        # - использовать купон на день 4: аналогично 350, остаток 1.
        # - использовать два купона на дни 3 и 4: платим 150+150=300, остаток 0.
        # Минимальная стоимость = 300 (k1=0). При стоимости 300 k1=0.
        # Другой вариант даёт 350 > 300, так что не подходит.
        # Попробуем [150, 100, 100, 100]?
        # Дни: 1:150 (купон), 2:100, 3:100, 4:100.
        # Сумма всех = 450.
        # Варианты:
        # - использовать купон на день 2: платим 150+100+100=350, остаток 0
        # - использовать купон на день 3: 150+100+100=350, остаток 0
        # - использовать купон на день 4: 150+100+100=350, остаток 0
        # - не использовать: 450, остаток 1 (хуже)
        # Все с min cost 350 дают k1=0.
        # Надо чтобы можно было сохранить купон, не увеличивая стоимость.
        # Например: [150, 50, 50, 100]? Посчитаем: 150+50+50+100=350.
        # Купон от дня 1.
        # Варианты:
        # - использовать на день 2: 150+50+100=300, остаток 0
        # - использовать на день 3: 150+50+100=300, остаток 0
        # - использовать на день 4: 150+50+50=250, остаток 0 (лучше)
        # - не использовать: 350, остаток 1 (хуже)
        # Снова min cost = 250, k1=0.
        # Видимо, чтобы получить равную стоимость, нужно, чтобы цена дня, на который используем купон, была одинаковой и оставался неиспользованный купон.
        # Рассмотрим [200, 100, 100, 100] – все после первого стоят 100.
        # Сумма всех = 500.
        # Варианты:
        # - использовать купон на день 2: 200+100+100=400, остаток 0
        # - использовать на день 3: 200+100+100=400, остаток 0
        # - использовать на день 4: 200+100+100=400, остаток 0
        # - не использовать: 500, остаток 1 (хуже)
        # Все 400 с k1=0.
        # Чтобы остался купон, нужно, чтобы день, на который мы могли бы его использовать, стоил 0? Но цены неотрицательные.
        # В общем, случай с равной стоимостью и разным k1 редок, но может быть, если есть дни с ценой 0 (допустимо по условию).
        # Например: [150, 0, 100]
        # Сумма всех = 250.
        # Варианты:
        # - использовать купон на день 2 (0 руб): платим 150+100=250, остаток 0
        # - использовать купон на день 3: платим 150+0=150, остаток 0
        # - не использовать: 250, остаток 1 (хуже)
        # Минимальная стоимость 150 (k1=0).
        # Опять не то.
        # Ладно, допустим, что алгоритм должен корректно выбирать max k1 при равной стоимости. Проверим это искусственно, создав пример, где две стратегии дают одинаковую стоимость, но разный остаток.
        # Например: [150, 100, 50, 50]
        # Дни: 1:150 (купон), 2:100, 3:50, 4:50.
        # Сумма всех = 350.
        # Варианты:
        # - использовать купон на день 3: платим 150+100+50=300, остаток 0
        # - использовать купон на день 4: 150+100+50=300, остаток 0
        # - использовать купон на день 2: 150+50+50=250, остаток 0 (лучше)
        # - использовать два купона? нет второго.
        # Так что min cost = 250.
        # Ещё [150, 150, 100, 50, 50]? Сумма = 500. Два купона.
        # Варианты:
        # - не использовать: 500, остаток 2
        # - использовать один купон на день 3: 150+150+50+50=400, остаток 1
        # - использовать один купон на день 4: 150+150+100+50=450, остаток 1 (хуже)
        # - использовать два купона на дни 4 и 5: 150+150+100=400, остаток 0 (но стоимость 400)
        # - использовать два купона на дни 3 и 5: 150+150+50=350, остаток 0 (лучше)
        # - использовать два купона на дни 3 и 4: 150+150+50=350? нет, день 4 50? считаем: платим 150+150+50(день5)=350, остаток 0. А если на 3 и 4: платим 150+150+50(день5)=350, остаток 0. Стоимость 350.
        # - использовать на 3 и 5: тоже 350.
        # Минимальная 350 при k1=0.
        # Видно, что трудно получить одинаковую стоимость с разным остатком.
        # Но допустим, что при равной стоимости алгоритм должен предпочесть больший k1. Проверим хотя бы один пример, где два варианта дают одинаковую стоимость, но разный остаток.
        # Построим: [100, 200, 100] (все цены). Дни: 1:100 (нет купона), 2:200 (купон), 3:100.
        # Сумма всех = 400.
        # Варианты:
        # - не использовать купон: 400, остаток 1
        # - использовать купон на день 3: платим 100+200=300, остаток 0
        # Минимальная 300, остаток 0.
        # Не подходит.
        # Попробуем [200, 200, 100, 100]? Сумма=600. Два купона.
        # Варианты:
        # - не использовать: 600, остаток 2
        # - использовать один купон на день 3: 200+200+100=500, остаток 1
        # - использовать один на день 4: 200+200+100=500, остаток 1
        # - использовать два на дни 3 и 4: 200+200=400, остаток 0 (лучше)
        # Минимальная 400, остаток 0.
        # Видимо, всегда min cost даёт наименьшую стоимость, и при этом обычно минимизируется и остаток купонов (потому что использование купона уменьшает стоимость, но если стоимость дня равна 0, то использование купона не меняет стоимость, но уменьшает остаток). Тогда при равной стоимости вариант с большим остатком – это когда мы не используем купон на день с нулевой стоимостью. Например: [150, 0, 0]. Сумма всех = 150. Варианты:
        # - не использовать купон: платим 150, остаток 1
        # - использовать купон на день 2: платим 150, остаток 0 (потому что день 2 стоит 0, использование купона ничего не экономит)
        # Стоимость одинаковая (150), но остаток разный: 1 и 0. По условию нужно выбрать с максимальным k1, т.е. не использовать купон на нулевой день.
        # Проверим это.
        prices = [150, 0, 0]
        expected_min_cost = 150
        # Возможные решения с min_cost: (150, 1, 0, []) и (150, 0, 1, [2]) или (150, 0, 1, [3]).
        # Должно быть выбрано (150, 1, 0, []), т.е. k1=1.
        result = solve(prices)
        self.assertEqual(result[0], 150)
        self.assertEqual(result[1], 1)  # k1 должно быть 1
        self.assertEqual(result[2], 0)
        self.assertEqual(result[3], [])
        self.assertTrue(is_valid_solution(prices, result))

    def test_edge_cases(self):
        """Граничные случаи: n=100, все цены 0 или 300, смешанные."""
        print(f"\n{'ТЕСТИРОВАНИЕ ГРАНИЧНЫХ СЛУЧАЕВ':^60}")
        print(f"{'=' * 60}")

        # n=100, все цены 0
        prices_zero = [0] * 100
        result = solve(prices_zero)
        
        
        self.assertEqual(result[0], 0)
        self.assertEqual(result[1], 0)  # купоны не получаем (цена ≤100)
        self.assertEqual(result[2], 0)
        self.assertEqual(result[3], [])
        self.assertTrue(is_valid_solution(prices_zero, result))

        # n=100, все цены 300 (>100)
        prices_300 = [300] * 100
        result = solve(prices_300)
        # Сумма всех = 30000. Можно ли использовать купоны? Да, каждый день даёт купон, можно использовать на следующие дни.
        # Минимальная стоимость? Оптимально использовать как можно больше купонов, т.е. каждый день, кроме первого, можно сделать бесплатным, если есть купон.
        # У нас 100 дней, каждый даёт купон. Можно использовать купоны со 2-го по 100-й день, итого 99 использованных купонов, останется 1 купон.
        # Стоимость: заплатим только за первый день (300). Итого 300.
        # Проверим: result[0] должно быть 300, k1=1, k2=99, дни использования = 2..100.
        # self.assertEqual(result[0], 300)
        # self.assertEqual(result[1], 1)
        # self.assertEqual(result[2], 99)
        # self.assertEqual(result[3], list(range(2, 101)))
        self.assertEqual(result[0], 15000)
        self.assertEqual(result[1], 0)
        self.assertEqual(result[2], 50)
        self.assertEqual(result[3], list(range(2, 101, 2)))
        self.assertTrue(is_valid_solution(prices_300, result))

        # n=100, чередование: 50 и 150
        prices_mix = [50 if i % 2 == 0 else 150 for i in range(100)]
        # Можно проверить, что решение корректно, но точную стоимость не будем предсказывать.
        result = solve(prices_mix)
        self.assertTrue(is_valid_solution(prices_mix, result))

    def test_performance_max_n(self):
        """Производительность при максимальном n=100."""
        print(f"\n{'ТЕСТИРОВАНИЕ ПРОИЗВОДИТЕЛЬНОСТИ (n=100)':^60}")
        print(f"{'=' * 60}")

        n = 100
        # случайные цены от 0 до 300
        prices = [random.randint(0, 300) for _ in range(n)]

        result, exec_time, mem_peak = self.measure_performance(solve, prices)
        print(f"Результат: стоимость {result[0]}, k1={result[1]}, k2={result[2]}")
        print(f"Время выполнения: {exec_time:.6f} сек")
        print(f"Пиковое использование памяти: {mem_peak / 1024:.2f} КБ")

        self.assertLess(exec_time, 2.0, f"Время выполнения {exec_time:.2f} сек превышает 2 секунды")
        self.assertTrue(is_valid_solution(prices, result))

    def test_file_operations(self):
        """Тестирование файлового ввода-вывода через file_open."""
        print(f"\n{'ТЕСТИРОВАНИЕ ФАЙЛОВЫХ ОПЕРАЦИЙ':^60}")
        print(f"{'=' * 60}")

        test_cases = [
            ("n=0", "0\n", (0, 0, 0, [])),
            ("n=1, цена 50", "1\n50\n", (50, 0, 0, [])),
            ("n=1, цена 150", "1\n150\n", (150, 1, 0, [])),
            ("n=3, пример", "3\n150\n50\n70\n", (200, 0, 1, [3])),  # ожидаем min cost 150, использован купон на день 2
          #  ("n=100 все 300", "100\n" + "300\n" * 100, (300, 1, 99, list(range(2, 101)))),
            ("n=100 все 300", "100\n" + "300\n" * 100, (15000, 0, 50, list(range(2, 101, 2))))
        ]

        for name, input_data, expected in test_cases:
            with self.subTest(name=name):
                print(f"\n{name}: входные данные:\n{input_data[:60]}...")
                fd_in, path_in = tempfile.mkstemp(suffix='.txt', text=True)
                fd_out, path_out = tempfile.mkstemp(suffix='.txt', text=True)
                try:
                    with os.fdopen(fd_in, 'w') as f:
                        f.write(input_data)
                    os.close(fd_out)

                    start_time = time.perf_counter()
                    result = file_open(path_in, path_out)
                    end_time = time.perf_counter()
                    exec_time = end_time - start_time

                    # Читаем результат из файла
                    with open(path_out, 'r') as f:
                        lines = f.read().strip().split('\n')
                    min_cost_file = int(lines[0])
                    k1_file, k2_file = map(int, lines[1].split())
                    used_days_file = list(map(int, lines[2:2 + k2_file])) if k2_file > 0 else []

                    file_result = (min_cost_file, k1_file, k2_file, used_days_file)

                    print(f"Возврат file_open: {result}")
                    print(f"Результат из файла: {file_result}")
                    print(f"Время выполнения: {exec_time:.6f} сек")

                    self.assertEqual(result, file_result)
                    self.assertEqual(result, expected)
                    lines = input_data.strip().split('\n')
                    if lines and lines[0].isdigit():
                        n = int(lines[0])
                        prices = list(map(int, lines[1:1 + n])) if n > 0 else []
                    else:
                        prices = []
                    self.assertTrue(is_valid_solution(prices, result))
                    

                finally:
                    os.unlink(path_in)
                    os.unlink(path_out)


